# Рассчитываем размах и стандартное отклонение с помощью numpy

import numpy as np
sample = np.array([185, 175, 170, 169, 171, 175, 157, 172, 170, 172, 167, 173, 168, 167, 166,
              167, 169, 172, 177, 178, 165, 161, 179, 159, 164, 178, 172, 170, 173, 171])

print(f'Размах: {np.ptp(sample)} равен max - min: {np.max(sample)- np.min(sample)}')

print(f'Стандартное отклонение: {np.std(sample):.2f}')
